export function mulawToLinear16(mulawData: Buffer): Buffer {
  const pcm = Buffer.alloc(mulawData.length * 2);
  for (let i = 0; i < mulawData.length; i++) {
    pcm.writeInt16LE(MULAW_DECODE_TABLE[mulawData[i]], i * 2);
  }
  return pcm;
}

export function linear16ToMulaw(pcmData: Buffer): Buffer {
  const mulaw = Buffer.alloc(pcmData.length / 2);
  for (let i = 0; i < mulaw.length; i++) {
    let sample = pcmData.readInt16LE(i * 2);
    const sign = (sample >> 8) & 0x80;
    if (sign !== 0) sample = -sample;
    if (sample > MULAW_CLIP) sample = MULAW_CLIP;
    sample += MULAW_BIAS;
    const exponent = MULAW_ENCODE_TABLE[(sample >> 7) & 0xff];
    const mantissa = (sample >> (exponent + 3)) & 0x0f;
    mulaw[i] = ~(sign | (exponent << 4) | mantissa) & 0xff;
  }
  return mulaw;
}

export function base64ToBuffer(base64: string): Buffer {
  return Buffer.from(base64, 'base64');
}

export function bufferToBase64(buffer: Buffer): string {
  return buffer.toString('base64');
}

export function resample(audio: Buffer, fromRate: number, toRate: number): Buffer {
  if (fromRate === toRate) return audio;
  const ratio = toRate / fromRate;
  const inputSamples = audio.length / 2;
  const outputSamples = Math.round(inputSamples * ratio);
  const output = Buffer.alloc(outputSamples * 2);

  for (let i = 0; i < outputSamples; i++) {
    const srcIndex = i / ratio;
    const srcIndexFloor = Math.floor(srcIndex);
    const srcIndexCeil = Math.min(srcIndexFloor + 1, inputSamples - 1);
    const fraction = srcIndex - srcIndexFloor;
    const a = audio.readInt16LE(srcIndexFloor * 2);
    const b = audio.readInt16LE(srcIndexCeil * 2);
    const interpolated = Math.round(a + (b - a) * fraction);
    output.writeInt16LE(Math.max(-32768, Math.min(32767, interpolated)), i * 2);
  }

  return output;
}
